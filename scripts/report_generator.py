# ─── Delivery ───────────────────────────────────────────────────────────────────

def deliver_email(report_content: str, subject: str, config: dict,
                   fmt: str, exec_summary: dict) -> bool:
    """Send report via SMTP. Returns True on success."""
    smtp_conf = config.get('delivery', {}).get('smtp', {})
    if not smtp_conf.get('enabled'):
        return False
    try:
        msg = email.mime.multipart.MIMEMultipart('alternative')
        msg['Subject'] = subject
        msg['From'] = smtp_conf.get('from', 'security@edgeiq.io')
        msg['To'] = ', '.join(smtp_conf.get('to', []))

        if fmt == 'html':
            msg.attach(email.mime.text.MIMEText(report_content, 'html'))
            # Plain fallback
            plain = generate_text_report(exec_summary, [], [], [], [], [], {})
            msg.attach(email.mime.text.MIMEText(plain, 'plain'))
        else:
            msg.attach(email.mime.text.MIMEText(report_content, 'plain'))

        if smtp_conf.get('attachments') and fmt == 'html':
            part = email.mime.base.MIMEBase('application', 'octet-stream')
            part.set_payload(report_content.encode('utf-8'))
            email.encoders.encode_base64(part)
            part.add_header('Content-Disposition', 'attachment', filename='security_report.html')
            msg.attach(part)

        context = ssl.create_default_context()
        with smtplib.SMTP(smtp_conf.get('host', 'localhost'),
                          smtp_conf.get('port', 587), timeout=30) as server:
            server.starttls(context=context)
            if smtp_conf.get('username') and smtp_conf.get('password'):
                server.login(smtp_conf['username'], smtp_conf['password'])
            server.sendmail(smtp_conf['from'], smtp_conf['to'], msg.as_string())
        return True
    except Exception as e:
        print(f"[EMAIL ERROR] {e}", file=sys.stderr)
        return False

def deliver_telegram(report_content: str, config: dict,
                     fmt: str, exec_summary: dict) -> bool:
    """Send report summary via Telegram bot. Returns True on success."""
    tg_conf = config.get('delivery', {}).get('telegram', {})
    if not tg_conf.get('enabled') or not tg_conf.get('bot_token'):
        return False
    try:
        import urllib.request
        import urllib.parse

        text = (f"🛡️ EdgeIQ Labs Security Report\n"
                f"Risk Score: {exec_summary.get('risk_score', 'N/A')}/100\n"
                f"Issues Found: {exec_summary.get('total_issues', 0)}\n"
                f"Targets: {exec_summary.get('targets_scanned', 0)}\n")

        url = f"https://api.telegram.org/bot{tg_conf['bot_token']}/sendMessage"
        data = urllib.parse.urlencode({
            'chat_id': tg_conf.get('chat_id', tg_conf.get('target_chat_id', '')),
            'text': text,
            'parse_mode': 'HTML',
        }).encode()
        req = urllib.request.Request(url, data=data)
        with urllib.request.urlopen(req, timeout=15) as resp:
            json.loads(resp.read())
        return True
    except Exception as e:
        print(f"[TELEGRAM ERROR] {e}", file=sys.stderr)
        return False

# ─── Subprocess Tool Runner ─────────────────────────────────────────────────────

def run_edgeiq_tool(tool_script: str, target: str, timeout: int = 120) -> dict:
    """Run an EdgeIQ tool script as a subprocess. Returns JSON dict or error dict."""
    import shutil
    import subprocess

    if not shutil.which('python3'):
        return {'error': 'python3 not found', 'tool': tool_script}

    try:
        result = subprocess.run(
            ['python3', tool_script, '--target', target, '--json'],
            capture_output=True, text=True, timeout=timeout,
        )
        if result.returncode == 0 and result.stdout.strip():
            return json.loads(result.stdout)
        return {'error': f'exit {result.returncode}', 'stderr': result.stderr[:500]}
    except subprocess.TimeoutExpired:
        return {'error': 'timeout', 'tool': tool_script, 'target': target}
    except FileNotFoundError:
        return {'error': 'script not found', 'tool': tool_script}
    except json.JSONDecodeError:
        return {'error': 'invalid JSON output', 'tool': tool_script}

