def generate_text_report(exec_summary: dict, xss_data: list, network_data: list,
                         ssl_data: list, alert_data: list, recs: list,
                         targets_data: dict) -> str:
    lines = []
    lines.append("=" * 70)
    lines.append("EDGEIQ LABS — SECURITY REPORT")
    lines.append("=" * 70)
    lines.append(f"Generated: {exec_summary['generated_at']}")
    lines.append(f"Targets Scanned: {exec_summary['targets_scanned']}")
    lines.append(f"Overall Risk Score: {exec_summary['risk_score']}/100 ({exec_summary['risk_label'].upper()})")
    lines.append("")

    sb = exec_summary['severity_breakdown']
    lines.append("── Severity Breakdown ──────────────────────────────")
    for sev in ('critical', 'high', 'medium', 'low', 'info'):
        count = sb.get(sev, 0)
        if count:
            lines.append(f"  [{sev.upper():8}] {count}")
    lines.append("")

    if xss_data:
        lines.append("── XSS Findings ─────────────────────────────────────")
        for f in xss_data:
            lines.append(f"  [{f['severity'].upper():8}] {f['vulnerability']}")
            lines.append(f"  Target: {f['target']} | URL: {f['url']}")
            if f.get('parameter'):
                lines.append(f"  Parameter: {f['parameter']}")
            if f.get('payload'):
                lines.append(f"  Payload: {f['payload']}")
            if f.get('description'):
                lines.append(f"  {f['description']}")
            lines.append("")
    else:
        lines.append("── XSS Findings: None")
        lines.append("")

    if network_data:
        lines.append("── Network Findings ────────────────────────────────")
        for section in network_data:
            lines.append(f"  Target: {section['target']} ({section.get('ip', 'N/A')})")
            if section.get('open_ports'):
                lines.append(f"  Open Ports ({len(section['open_ports'])}):")
                for p in section['open_ports'][:20]:
                    lines.append(f"    {p.get('port','?')}/{p.get('protocol','tcp')} - "
                                 f"{p.get('service','unknown')} (severity: {p.get('severity','medium')})")
            if section.get('cves'):
                lines.append(f"  CVEs ({len(section['cves'])}):")
                for c in section['cves'][:10]:
                    lines.append(f"    {c.get('cve_id','N/A')} - {c.get('description','')[:70]}")
            lines.append("")
    else:
        lines.append("── Network Findings: None")
        lines.append("")

    if ssl_data:
        lines.append("── SSL/Certificate Findings ────────────────────────")
        for section in ssl_data:
            lines.append(f"  Domain: {section['domain']} | Grade: {section['grade']}")
            lines.append(f"  Issuer: {section['issuer']}")
            lines.append(f"  Valid Until: {section['valid_until']} ({section.get('days_until_expiry','?')} days)")
            if section.get('issues'):
                for issue in section['issues']:
                    lines.append(f"  Issue: [{issue.get('severity','?').upper()}] {issue.get('title','N/A')}")
            lines.append("")
    else:
        lines.append("── SSL/Certificate Findings: None")
        lines.append("")

    if alert_data:
        lines.append(f"── Alert History ({len(alert_data)} events) ───────────────")
        for a in alert_data[:20]:
            lines.append(f"  [{a.get('severity','?').upper():8}] {a.get('title', a.get('name','N/A'))}")
            lines.append(f"  Target: {a.get('target','N/A')} | Time: {a.get('timestamp', a.get('time','N/A'))}")
            lines.append("")
    else:
        lines.append("── Alert History: No alerts recorded")
        lines.append("")

    lines.append("── Recommendations ────────────────────────────────")
    for r in recs:
        lines.append(f"  Priority {r['priority']} [{r['severity'].upper():8}] {r['title']}")
        lines.append(f"  {r['detail']}")
        lines.append("")

    lines.append(BRAND_FOOTER)
    return "\n".join(lines)

