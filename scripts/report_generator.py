def main():
    parser = argparse.ArgumentParser(description='EdgeIQ Labs Security Report Generator')
    parser.add_argument('--config', default='config.json', help='Path to config JSON file')
    parser.add_argument('--format', choices=['html', 'text', 'json'], default='html', help='Output format')
    parser.add_argument('--delivery', choices=['email', 'telegram', 'file', 'both'], default='file', help='Delivery method')
    parser.add_argument('--targets', nargs='*', help='Override target list from config')
    parser.add_argument('--input-dir', help='Override scan input directory')
    parser.add_argument('--output-dir', help='Override output directory')
    parser.add_argument('--title', default='Monthly Security Report', help='Report title')
    parser.add_argument('--throttle-ms', type=int, default=1000, help='Delay between targets (ms)')
    parser.add_argument('--no-run-scans', action='store_true', help='Skip running EdgeIQ tool subprocesses')
    args = parser.parse_args()

    config = load_config(args.config)

    scan_dir = args.input_dir or config.get('scan_input_dir', 'input/scans')
    output_dir = args.output_dir or config.get('output', {}).get('path', 'output')
    target_list = args.targets or config.get('targets', [])
    os.makedirs(output_dir, exist_ok=True)

    # Gather scan data from input directory
    targets_data: dict = {}
    dir_scan_data = scan_input_directory(scan_dir)
    for target_name, data in dir_scan_data.items():
        targets_data[target_name] = data

    # Run EdgeIQ tool subprocesses if configured
    if not args.no_run_scans and target_list:
        tool_map = config.get('edgeiq_tools', {})
        for target in target_list:
            target_info = parse_target(target)
            tname = target_info['domain']
            if tname not in targets_data:
                targets_data[tname] = {'xss': [], 'network': None, 'ssl': None, 'alerts': []}

            xss_script = tool_map.get('xss_scanner', '')
            if xss_script and os.path.exists(xss_script):
                result = run_edgeiq_tool(xss_script, target)
                if result and 'error' not in result:
                    targets_data[tname]['xss'].extend(result.get('findings', result.get('vulnerabilities', [])))
                time.sleep(args.throttle_ms / 1000)

            net_script = tool_map.get('network_scanner', '')
            if net_script and os.path.exists(net_script):
                result = run_edgeiq_tool(net_script, target)
                if result and 'error' not in result:
                    targets_data[tname]['network'] = result
                time.sleep(args.throttle_ms / 1000)

            ssl_script = tool_map.get('ssl_watcher', '')
            if ssl_script and os.path.exists(ssl_script):
                result = run_edgeiq_tool(ssl_script, target)
                if result and 'error' not in result:
                    targets_data[tname]['ssl'] = result
                time.sleep(args.throttle_ms / 1000)

    # Build report sections
    global _report_targets_data
    _report_targets_data = targets_data

    exec_summary = build_executive_summary(targets_data)
    xss_data = build_xss_section(targets_data)
    network_data = build_network_section(targets_data)
    ssl_data = build_ssl_section(targets_data)
    alert_data = build_alerts_section(targets_data)
    recs = build_recommendations(exec_summary, xss_data, network_data, ssl_data, alert_data)

    # Generate output
    if args.format == 'html':
        report_content = generate_html_report(exec_summary, xss_data, network_data, ssl_data, alert_data, recs, targets_data, args.title)
        ext = 'html'
    elif args.format == 'text':
        report_content = generate_text_report(exec_summary, xss_data, network_data, ssl_data, alert_data, recs, targets_data)
        ext = 'txt'
    else:
        report_content = json.dumps(generate_json_report(exec_summary, xss_data, network_data, ssl_data, alert_data, recs, targets_data), indent=2)
        ext = 'json'

    timestamp = datetime.datetime.utcnow().strftime('%Y%m%d_%H%M%S')
    report_filename = f"security_report_{timestamp}.{ext}"
    report_path = os.path.join(output_dir, report_filename)

    with open(report_path, 'w', encoding='utf-8') as f:
        f.write(report_content)

    print(f"Report written to: {report_path}")

    # Delivery
    subject = (f"EdgeIQ Labs Security Report — {args.title} "
               f"({exec_summary['risk_score']}/100 {exec_summary['risk_label'].upper()})")

    if args.delivery in ('email', 'both'):
        ok = deliver_email(report_content, subject, config, args.format, exec_summary)
        print(f"Email delivery: {'✓' if ok else '✗'}")

    if args.delivery in ('telegram', 'both'):
        ok = deliver_telegram(report_content, config, args.format, exec_summary)
        print(f"Telegram delivery: {'✓' if ok else '✗'}")

    print(f"Done. {exec_summary['total_issues']} issues found, risk score: {exec_summary['risk_score']}/100")

if __name__ == '__main__':
    main()