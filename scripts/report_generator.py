def build_executive_summary(targets_data: dict) -> dict:
    sev_counts = {'critical': 0, 'high': 0, 'medium': 0, 'low': 0, 'info': 0}
    total_issues = 0
    xss_total = 0
    open_ports = 0
    ssl_issues = 0
    alert_count = 0
    targets_scanned = len(targets_data)

    for target, data in targets_data.items():
        for finding in data.get('xss', []):
            sev = finding.get('severity', 'medium').lower()
            sev_counts[sev] = sev_counts.get(sev, 0) + 1
            total_issues += 1
            xss_total += 1

        if data.get('network'):
            for port in data['network'].get('open_ports', []):
                open_ports += 1
                sev = port.get('severity', 'medium').lower()
                sev_counts[sev] = sev_counts.get(sev, 0) + 1
                total_issues += 1
            for cve in data['network'].get('cves', []):
                sev = cve.get('severity', 'high').lower()
                sev_counts[sev] = sev_counts.get(sev, 0) + 1
                total_issues += 1

        if data.get('ssl'):
            for issue in data['ssl'].get('issues', []):
                ssl_issues += 1
                sev = issue.get('severity', 'medium').lower()
                sev_counts[sev] = sev_counts.get(sev, 0) + 1
                total_issues += 1

        alert_count += len(data.get('alerts', []))

    risk_score = compute_risk_score(sev_counts)
    risk_label = risk_score_to_label(risk_score)

    return {
        'generated_at': now_iso(),
        'targets_scanned': targets_scanned,
        'total_issues': total_issues,
        'risk_score': risk_score,
        'risk_label': risk_label,
        'severity_breakdown': sev_counts,
        'xss_findings': xss_total,
        'open_ports': open_ports,
        'ssl_issues': ssl_issues,
        'alert_count': alert_count,
    }

def build_xss_section(targets_data: dict) -> list:
    findings = []
    order = {'critical': 0, 'high': 1, 'medium': 2, 'low': 3, 'info': 4}
    for target, data in targets_data.items():
        for f in data.get('xss', []):
            findings.append({
                'target': target,
                'url': f.get('url', target),
                'vulnerability': f.get('vulnerability', f.get('type', 'Unknown')),
                'severity': f.get('severity', 'medium'),
                'description': f.get('description', ''),
                'payload': f.get('payload', ''),
                'parameter': f.get('parameter', f.get('param', '')),
                'evidence': f.get('evidence', ''),
            })
    findings.sort(key=lambda x: order.get(x['severity'].lower(), 5))
    return findings

def build_network_section(targets_data: dict) -> list:
    sections = []
    for target, data in targets_data.items():
        if not data.get('network'):
            continue
        net = data['network']
        sections.append({
            'target': target,
            'ip': net.get('ip', net.get('host', '')),
            'open_ports': sorted(net.get('open_ports', []), key=lambda p: p.get('port', 0)),
            'cves': net.get('cves', []),
            'os_detection': net.get('os', net.get('os_detection', '')),
            'service_versions': net.get('service_versions', {}),
        })
    return sections

def build_ssl_section(targets_data: dict) -> list:
    sections = []
    for target, data in targets_data.items():
        if not data.get('ssl'):
            continue
        ssl_data = data['ssl']
        sections.append({
            'target': target,
            'domain': ssl_data.get('domain', target),
            'issuer': ssl_data.get('issuer', ''),
            'valid_from': ssl_data.get('valid_from', ''),
            'valid_until': ssl_data.get('valid_until', ''),
            'days_until_expiry': ssl_data.get('days_until_expiry', 0),
            'protocol_version': ssl_data.get('protocol_version', 'TLS 1.2'),
            'grade': ssl_data.get('grade', 'N/A'),
            'issues': ssl_data.get('issues', []),
            'headers': ssl_data.get('security_headers', {}),
        })
    return sections

def build_alerts_section(targets_data: dict) -> list:
    all_alerts = []
    order = {'critical': 0, 'high': 1, 'medium': 2, 'low': 3, 'info': 4}
    for target, data in targets_data.items():
        for alert in data.get('alerts', []):
            alert_copy = dict(alert)
            alert_copy['target'] = target
            all_alerts.append(alert_copy)
    all_alerts.sort(key=lambda x: order.get(x.get('severity', 'medium').lower(), 5))
    return all_alerts

def build_recommendations(exec_summary: dict, xss_data: list, network_data: list,
                           ssl_data: list, alert_data: list) -> list:
    recs = []
    sev_order = {'critical': 0, 'high': 1, 'medium': 2, 'low': 3}

    critical_xss = [f for f in xss_data if f['severity'].lower() in ('critical', 'high')]
    if critical_xss:
        recs.append({
            'priority': 1, 'severity': 'critical',
            'title': 'Address Critical/High XSS Vulnerabilities Immediately',
            'detail': (f'{len(critical_xss)} critical or high severity cross-site scripting vulnerabilities found. '
                      'These can lead to session hijacking, credential theft, and defacement. '
                      'Implement input sanitization and output encoding. Consider deploying a WAF as temporary mitigation.'),
        })

    critical_cves = [c for section in network_data for c in section.get('cves', [])
                     if c.get('severity', '').lower() in ('critical', 'high')]
    if critical_cves:
        recs.append({
            'priority': 2, 'severity': 'critical',
            'title': 'Patch Critical CVEs Within 24-48 Hours',
            'detail': (f'{len(critical_cves)} critical or high severity CVEs detected. '
                      'Prioritize patching by CVSS score. Subscribe to CVE feeds and patch systematically.'),
        })

    ssl_critical = [f"{s['target']}: {i.get('title', 'SSL issue')}"
                    for s in ssl_data for i in s.get('issues', [])
                    if i.get('severity', '').lower() in ('critical', 'high')]
    if ssl_critical:
        recs.append({
            'priority': 3, 'severity': 'critical',
            'title': 'Fix Critical SSL/TLS Configuration Issues',
            'detail': f'{len(ssl_critical)} critical SSL issues: ' + '; '.join(ssl_critical[:5]),
        })

    expiring = [s for s in ssl_data if s.get('days_until_expiry', 999) <= 30]
    if expiring:
        recs.append({
            'priority': 4, 'severity': 'high',
            'title': 'Renew Expiring SSL Certificates',
            'detail': (f'{len(expiring)} certificate(s) expiring within 30 days. '
                      'Renew before expiry to avoid service disruptions.'),
        })

    sensitive = [f"{p['port']}/{p['service']}" for section in network_data
                  for p in section.get('open_ports', [])
                  if p.get('service', '').lower() in ('ssh', 'ftp', 'telnet', 'rdp', 'mysql')]
    if sensitive:
        recs.append({
            'priority': 5, 'severity': 'medium',
            'title': 'Restrict Access to Sensitive Services',
            'detail': (f'Exposed sensitive ports: {", ".join(sensitive[:8])}. '
                      'Restrict via firewall rules, VPN, or jump hosts.'),
        })

    if alert_data:
        critical_alerts = [a for a in alert_data if a.get('severity', '').lower() in ('critical', 'high')]
        if critical_alerts:
            recs.append({
                'priority': 6, 'severity': 'high',
                'title': 'Review Triggered Security Alerts',
                'detail': f'{len(critical_alerts)} critical/high alerts generated. '
                          'Review alert history in the EdgeIQ Alerting System dashboard.',
            })

    if not recs:
        recs.append({
            'priority': 1, 'severity': 'low',
            'title': 'Continue Monitoring — No Critical Issues Found',
            'detail': 'All scanned targets passed with no critical or high severity findings. '
                      'Maintain regular scanning cadence to catch emerging issues.',
        })

    recs.sort(key=lambda x: (sev_order.get(x['severity'].lower(), 5), x['priority']))
    return recs

