# ─── Helpers ──────────────────────────────────────────────────────────────────

def load_config(path: str) -> dict:
    with open(path, 'r') as f:
        return json.load(f)

def load_json_file(path: str) -> Any:
    try:
        with open(path, 'r') as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return None

def now_iso() -> str:
    return datetime.datetime.utcnow().strftime('%Y-%m-%dT%H:%M:%SZ')

def severity_color(sev: str) -> str:
    colors = {
        'critical': '#ff2244',
        'high': '#ff4757',
        'medium': '#ffb800',
        'low': '#00c97a',
        'info': '#3498db',
    }
    return colors.get(sev.lower(), '#7a8499')

def severity_pill_class(sev: str) -> str:
    return f'sev-{sev.lower()}'

def alert_badge_class(sev: str) -> str:
    return f'alert-{sev.lower()}'

def risk_score_to_label(score: int) -> str:
    if score >= 80:
        return 'critical'
    elif score >= 60:
        return 'high'
    elif score >= 40:
        return 'medium'
    elif score >= 20:
        return 'low'
    return 'minimal'

def compute_risk_score(finding_counts: dict) -> int:
    """Weighted risk score 0-100. critical=25, high=18, medium=10, low=4, info=2."""
    weights = {'critical': 25, 'high': 18, 'medium': 10, 'low': 4, 'info': 2}
    score = sum(weights.get(sev.lower(), 0) * count for sev, count in finding_counts.items())
    return min(score, 100)

def html_escape(s: str) -> str:
    return html.escape(str(s), quote=True)

def parse_target(target_str: str) -> dict:
    target = target_str.strip().rstrip('/')
    if target.startswith('http://') or target.startswith('https://'):
        return {'url': target, 'domain': target.split('/')[2]}
    return {'domain': target, 'url': f'https://{target}'}

# ─── Input Directory Scanner ────────────────────────────────────────────────────

def scan_input_directory(scan_dir: str) -> dict:
    """
    Walk scan_dir for JSON files produced by EdgeIQ tools.
    Files named: xss_{target}.json, network_{target}.json,
                 ssl_{target}.json, alert_{target}.json
    Returns dict: { target: { 'xss': [], 'network': {}, 'ssl': {}, 'alerts': [] } }
    """
    targets: dict = {}
    scan_path = Path(scan_dir)
    if not scan_path.exists():
        return targets

    for json_file in sorted(scan_path.glob('*.json')):
        data = load_json_file(str(json_file))
        if not data:
            continue

        stem = json_file.stem.lower()
        tool: Optional[str] = None
        target_name = json_file.stem

        if stem.startswith('xss_'):
            tool = 'xss'
            target_name = stem[4:]
        elif stem.startswith('network_'):
            tool = 'network'
            target_name = stem[8:]
        elif stem.startswith('ssl_') or stem.startswith('cert_'):
            tool = 'ssl'
            target_name = stem[4:] if stem.startswith('ssl_') else stem[5:]
        elif stem.startswith('alert_'):
            tool = 'alerts'
            target_name = stem[6:]
        else:
            tool = data.get('tool', data.get('type', '')).lower()
            target_name = data.get('target', data.get('host', data.get('domain', json_file.stem)))

        if target_name not in targets:
            targets[target_name] = {'xss': [], 'network': None, 'ssl': None, 'alerts': []}

        if tool == 'xss':
            targets[target_name]['xss'].extend(data.get('findings', data.get('vulnerabilities', [])))
        elif tool == 'network':
            targets[target_name]['network'] = data
        elif tool == 'ssl':
            targets[target_name]['ssl'] = data
        elif tool == 'alerts':
            targets[target_name]['alerts'].extend(data.get('alerts', data.get('events', [])))

    return targets

